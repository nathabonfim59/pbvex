// TestEmptyEnvIncludedInHashInput verifies that an explicit empty env map
// (present but empty) is included in the component hash input, matching the
// TS bundler/protocol which includes env:{} when env is defined. A nil env
// (absent) is excluded.
func TestEmptyEnvIncludedInHashInput(t *testing.T) {
	base := ComponentDefinition{
		ModulePaths:  []string{"m.ts"},
		ModuleHashes: map[string]string{"m.ts": "e3b0c44298fc1c149afbf4c8996fb92427ae41e4649b934ca495991b7852b855"},
		Dependencies: []string{},
	}
	bundleSha := "abc123"

	withEnv := base
	withEnv.Env = map[string]EnvArgDescriptor{} // present but empty

	inputWith := componentHashInput(withEnv, bundleSha)
	_, hasEnv := inputWith["env"]
	if !hasEnv {
		t.Fatal("expected env key in hash input when Env is non-nil empty map")
	}
	envMap, ok := inputWith["env"].(map[string]any)
	if !ok || len(envMap) != 0 {
		t.Fatalf("expected empty env map, got %#v", inputWith["env"])
	}

	inputWithout := componentHashInput(base, bundleSha)
	_, hasEnv = inputWithout["env"]
	if hasEnv {
		t.Fatal("expected no env key in hash input when Env is nil")
	}

	// Different hashes: including env:{} changes the componentId.
	idWith := ComputeComponentID(withEnv, bundleSha)
	idWithout := ComputeComponentID(base, bundleSha)
	if idWith == idWithout {
		t.Fatal("expected different componentIds for empty-env-present vs env-absent")
	}
}

