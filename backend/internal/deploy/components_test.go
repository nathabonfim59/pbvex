func TestValidateComponentFunctionBinding(t *testing.T) {
	def := ComponentDefinition{ComponentID: "c", ModulePaths: []string{"messages.ts"}}
	graph := &ComponentGraph{
		Definitions: []ComponentDefinition{def},
		Mounts:      []ComponentMount{{Name: "counter", ComponentID: "c"}},
	}

	valid := []FunctionDescriptor{
		{Name: "fn", ModulePath: "pbvex/components/counter/messages.ts"},
		{Name: "root", ModulePath: "pbvex/root.ts"},
	}
	if err := validateComponentFunctionBinding(valid, graph); err != nil {
		t.Fatalf("expected valid binding: %v", err)
	}

	orphan := []FunctionDescriptor{
		{Name: "fn", ModulePath: "pbvex/components/counter/widgets.ts"},
	}
	if err := validateComponentFunctionBinding(orphan, graph); err == nil {
		t.Fatal("expected orphan function to fail binding")
	}

	nomatch := []FunctionDescriptor{
		{Name: "fn", ModulePath: "pbvex/components/nowhere/messages.ts"},
	}
	if err := validateComponentFunctionBinding(nomatch, graph); err == nil {
		t.Fatal("expected unmatched mount prefix to fail binding")
	}
}

func TestValidateComponentFunctionBindingNestedMounts(t *testing.T) {
	def := ComponentDefinition{ComponentID: "c", ModulePaths: []string{"messages.ts"}}
	graph := &ComponentGraph{
		Definitions: []ComponentDefinition{def},
		Mounts: []ComponentMount{{
			Name:        "parent",
			ComponentID: "c",
			Children: []ComponentMount{{
				Name:        "child",
				ComponentID: "c",
			}},
		}},
	}
	// Longest-prefix match: parent has prefix pbvex/components/parent/, child
	// has pbvex/components/parent/child/. The longest match wins.
	cases := []struct {
		modulePath string
		ok         bool
	}{
		{"pbvex/components/parent/messages.ts", true},
		{"pbvex/components/parent/child/messages.ts", true},
		{"pbvex/components/parent/other.ts", false},
	}
	for _, tc := range cases {
		err := validateComponentFunctionBinding([]FunctionDescriptor{{Name: "fn", ModulePath: tc.modulePath}}, graph)
		if tc.ok && err != nil {
			t.Errorf("%s: expected ok, got %v", tc.modulePath, err)
		}
		if !tc.ok && err == nil {
			t.Errorf("%s: expected error", tc.modulePath)
		}
	}
}

